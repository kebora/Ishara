package com.simiyudaniel.ishara2;

import android.content.Intent;
import android.os.Bundle;
import android.widget.Button;
import android.widget.Toast;

import androidx.activity.EdgeToEdge;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class CustomActivity extends AppCompatActivity {

    private Button btMainActivity;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_custom);
        //
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });
        //
        btMainActivity = findViewById(R.id.btMainActivity);

        btMainActivity.setOnClickListener(v->{
//            Toast.makeText(this,"Button has been clicked!",Toast.LENGTH_SHORT).show();
            Intent intent = new Intent(CustomActivity.this,MainActivity.class);
            startActivity(intent);

        });
    }
}